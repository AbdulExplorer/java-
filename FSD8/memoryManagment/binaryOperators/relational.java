package memoryManagment.binaryOperators;

//relational operators : 
//   == (equal to) 
//   != (not equal to) 
//   <  (less than) 
//   >  (greater than)
//   <= (less than equal to)
//   >= (greater than equal to)

public class relational 
{   
        public static void main(String[] args)
    {
        int a=42;
        int b=36;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }  
}
